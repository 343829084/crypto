func Test_DH64_GO(t *testing.T) {
	for i := 0; i < 10000; i += 2 {
		privateKey1, publicKey1 := Pair()
		privateKey2, publicKey2 := Pair()

		secret1 := Secret(privateKey1, publicKey2)
		secret2 := Secret(privateKey2, publicKey1)
		unitest.Pass(t, secret1 == secret2)
	}
}

